export function frequencyLabel(value: string, locale: AdminLocale) {
  const labels = {
    weekly: adminText[locale].weekly,
    every_2_weeks: adminText[locale].every2Weeks,
    every_3_weeks: adminText[locale].every3Weeks,
    monthly: adminText[locale].monthly,
    one_time: adminText[locale].oneTime,
    custom: adminText[locale].custom,
    unknown: adminText[locale].unknown
  };
  return labels[value as keyof typeof labels] || value.replaceAll("_", " ");
}

