export type AdminLocale = "es" | "en";

export function getAdminLocale(value: string | undefined): AdminLocale {
  return value === "en" ? "en" : "es";
}

export function langQuery(locale: AdminLocale) {
  return locale === "en" ? "?lang=en" : "";
}

